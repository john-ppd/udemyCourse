# using ** and //
#** raises to the power of
print(2**3) #will be 2 ^ 3 = 8

#// will divide by the number and then round down, (floor after division)
print(5//3)# will be 1.67, then round down
print(5//6)#will be 0.83 and then round down

#% will return remainder of division
print(6%4) # equals 2 because 4 goes into 6 once with a remainder of 2

